pub struct Logger;

impl Logger {
    pub fn error(input: &str) {
        println!("FATAL {}", input);
    }
    
    pub fn info(input: &str) {
        println!("INFO {}", input);
    }

    pub fn warn(input: &str) {
        println!("WARN {}", input);
    }

    pub fn success(input: &str) {
        println!("SUCCESS {}", input);
    }
}

